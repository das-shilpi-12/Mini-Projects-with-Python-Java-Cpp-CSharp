//floating point arithmetic
using System;
class sample{
    public static void Main(){
        float a = 20.5F;
        float b = 6.4F;
        Console.WriteLine("a = "+a);
        Console.WriteLine("b = "+b);
        Console.WriteLine("a<b = "+(a<b));
        Console.WriteLine("a>=b = "+(a>=b));
        Console.WriteLine("a==b = "+(a==b));
        Console.WriteLine("a!=b = "+(a!=b));
        Console.WriteLine("a>a+b = "+(a>a+b));
    }
}
